package fundamentos;

public class exercicio02 {
	public static void main (String args[]) {
		int a = 10;
		int b = -10;
		boolean c = true;
		boolean d = false;
		System.out.println(~a);// -11 (inverte o sinal e subtrai 1)
		System.out.println(~b);// 9 (inverte o sinal e subtrai 1)
		System.out.println(!c);// false (oposto valor booleano)
		System.out.println(!d);// true (oposto valor booleano)
	}
}

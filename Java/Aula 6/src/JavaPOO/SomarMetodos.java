package JavaPOO;

public class SomarMetodos {
	int somar (int a, int b) {
		return a + b;
	}
	int somar (int a, int b, int c) {
		return a + b + c;
	}
	
	int multiplicar (int a, int b) {
		return a * b;
	}
	int multiplicar (int a, int b, int c) {
		return a * b * c;
	}
/*Em java é possível criar vários métodos de mesmo nome
 * mas com formas diferentes */
}
